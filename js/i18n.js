/* Motor de traduccion */

let currentLang = localStorage.getItem('lang') || 'es';

/**
 * Devuelve el texto traducido para una clave dada.
 * @param {string} key
 * @returns {string}
 */

function t(key) {
    return TRANSLATIONS[currentLang][key] ?? TRANSLATIONS['es'][key] ?? key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        const value = t(key);
        if (value === undefined) return;

        if (el.hasAttribute('data-i18n-html')) {
            el.innerHTML = value;
        } else {
            el.textContent = value;
        }
    });

    //Placeholders de inputs
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });

    // Actualizar el lang del html para accesibilidad y SEO
    document.documentElement.lang = currentLang;

    //Actualizar boton de idioma
    const btn = document.getElementById('lang-toggle');
    if (btn) {
        btn.textContent = currentLang === 'es' ? 'EN' : 'ES';
        btn.setAttribute('aria-label',
            currentLang === 'es' ? 'Switch to English' : 'Cambiar a Español'
        );
    }

    // Comunica al sistema de typing para reiniciar con roles del idioma actual
    document.dispatchEvent(new CustomEvent('langChanged', {
        detail: { lang: currentLang, roles: t('roles') }
    }));
}

/*Alterna entre ES y EN y guarda la preferencia*/
function toggleLanguage() {
    currentLang = currentLang === 'es' ? 'en' : 'es';
    localStorage.setItem('lang', currentLang);
    applyTranslations();
}

/*Expone las funciones necesarias globalmente*/
window.i18n = { t, applyTranslations, toggleLanguage, getCurrentLang: () => currentLang };

//Aplica al cargar
document.addEventListener('DOMContentLoaded', applyTranslations);